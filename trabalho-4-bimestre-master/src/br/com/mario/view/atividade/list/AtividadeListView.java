    private void tbAtividadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAtividadesMouseClicked
        int rowSelect = tbAtividades.getSelectedRow();

        AtividadeTableModel tableModel = (AtividadeTableModel) tbAtividades.getModel();

        if (rowSelect != -1) {

            Atividade atividade = tableModel.getValueAtRow(rowSelect);

            //Edit
            if (tbAtividades.getSelectedColumn() == 2) {
                edit(atividade);
            }
            //Remove
            if (tbAtividades.getSelectedColumn() == 3) {
                remove(atividade);
            }
        }
    }//GEN-LAST:event_tbAtividadesMouseClicked

