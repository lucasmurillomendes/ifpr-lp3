    /**
     * Função responsavel pela remoção
     *
     * @param atividade
     */
    private void remove(Atividade atividade) {
        controller.remove(atividade);
        controller.loadAtividades();
    }

    /*/*
     Função responsavel pela edição
     */
    private void edit(Atividade atividade) {
        boolean closeDialog = new AtividadeFormView(this, true, atividade).openDialog();

        if (closeDialog) {
            controller.loadAtividades();
        }
    }

