    private void tbChamadasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbChamadasMouseClicked
        int rowSelect = tbChamadas.getSelectedRow();

        ChamadaTableModel tableModel = (ChamadaTableModel) tbChamadas.getModel();

        if (rowSelect != -1) {

            Chamada chamada = tableModel.getValueAtRow(rowSelect);

            //Edit
            if (tbChamadas.getSelectedColumn() == 3) {
                edit(chamada);
            }
        }
    }//GEN-LAST:event_tbChamadasMouseClicked

