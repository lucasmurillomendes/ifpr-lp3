    /*/*
     Função responsavel pela edição
     */
    private void edit(Chamada chamada) {
        boolean closeDialog;
        try {
            closeDialog = new ChamadaFormView(this, true, chamada).openDialog();
            if (closeDialog) {
                controller.loadChamadas();
            }
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

    }

